-- CREATING TABLE 'BRANCH'
CREATE TABLE BRANCH (BRANCH_NO VARCHAR(10) PRIMARY KEY, MANAGER_ID VARCHAR(10), BRANCH_ADDRESS VARCHAR(30), CONTACT_NO VARCHAR(15));
DESC BRANCH;

-- CREATE TABLE 'EMPLOYEE'
CREATE TABLE EMPLOYEE (EMP_ID VARCHAR(10) PRIMARY KEY, EMP_NAME VARCHAR(30), POSITION VARCHAR(30), SALARY DECIMAL(10, 2));
DESC EMPLOYEE;

-- CREATE TABLE 'CUSTOMER'
CREATE TABLE CUSTOMER (CUSTOMER_ID VARCHAR(10) PRIMARY KEY, CUSTOMER_NAME VARCHAR(30), CUSTOMER_ADDRESS VARCHAR(30), REG_DATE DATE);
DESC CUSTOMER;

-- CREATE TABLE 'BOOKS'
CREATE TABLE BOOKS (ISBN VARCHAR(10) PRIMARY KEY, BOOK_TITLE VARCHAR(50), CATEGORY VARCHAR(30), RENTAL_PRICE DECIMAL(10,2), Status ENUM('Yes','No'), AUTHOR VARCHAR(30), PUBLISHER VARCHAR(30));
DESC BOOKS;
ALTER TABLE BOOKS MODIFY COLUMN ISBN VARCHAR(25);
ALTER TABLE BOOKS MODIFY COLUMN BOOK_TITLE VARCHAR(80);

-- CREATE TABLE 'ISSUE STATUS'
CREATE TABLE ISSUESTATUS (ISSUE_ID VARCHAR(10) PRIMARY KEY, ISSUED_CUST VARCHAR(30), ISSUED_BOOK_NAME VARCHAR(50), ISSUE_DATE DATE, ISBN_BOOK VARCHAR(15), FOREIGN KEY (ISSUED_CUST) REFERENCES CUSTOMER(CUSTOMER_ID) ON DELETE CASCADE, FOREIGN KEY (ISBN_BOOK) REFERENCES BOOKS(ISBN) ON DELETE CASCADE);
DESC ISSUESTATUS;
ALTER TABLE ISSUESTATUS MODIFY COLUMN ISSUED_BOOK_NAME VARCHAR(80);
ALTER TABLE ISSUESTATUS MODIFY COLUMN ISBN_BOOK VARCHAR(25);

-- CREATE TABLE 'RETURNSTATUS'
CREATE TABLE RETURNSTATUS (RETURN_ID VARCHAR(10) PRIMARY KEY, RETURN_CUST VARCHAR(30), RETURN_BOOK_NAME VARCHAR(50), RETURN_DATE DATE, ISBN_BOOK2 VARCHAR(15), FOREIGN KEY (ISBN_BOOK2) REFERENCES BOOKS(ISBN) ON DELETE CASCADE);
DESC RETURNSTATUS;
ALTER TABLE RETURNSTATUS MODIFY COLUMN RETURN_BOOK_NAME VARCHAR(80);
ALTER TABLE RETURNSTATUS MODIFY COLUMN ISBN_BOOK2 VARCHAR(25);

